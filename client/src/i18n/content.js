function pickLocalized(locale, bucket, id, item) {
  if (!item || locale !== 'uz') return item;
  const tr = bucket?.[id];
  if (!tr) return item;
  return { ...item, ...tr };
}

export function localizeModule(locale, mod) {
  return pickLocalized(locale, localizedContent.uz?.modules, mod?.id, mod);
}

export function localizeTestSection(locale, section) {
  return pickLocalized(locale, localizedContent.uz?.tests, section?.id, section);
}

export function localizeLab(locale, lab) {
  return pickLocalized(locale, localizedContent.uz?.labs, lab?.id, lab);
}

export function localizeVideoCourse(locale, course) {
  return pickLocalized(locale, localizedContent.uz?.videoCourses, course?.id, course);
}

export function localizeVideo(locale, video) {
  return pickLocalized(locale, localizedContent.uz?.videos, video?.id, video);
}

export function localizeCtf(locale, challenge) {
  return pickLocalized(locale, localizedContent.uz?.ctf, challenge?.id, challenge);
}

export function localizeResource(locale, resource) {
  return pickLocalized(locale, localizedContent.uz?.resources, resource?.id, resource);
}
